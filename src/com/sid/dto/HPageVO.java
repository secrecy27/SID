package com.sid.dto;

public class HPageVO {
	private int hpageId;
	private String timageUrl;
	private String dimageUrl;
	private int tprice;
	private int dprice;
	private int price;
	
	public int getHpageId() {
		return hpageId;
	}
	public void setHpageId(int hpageId) {
		this.hpageId = hpageId;
	}
	public String getTimageUrl() {
		return timageUrl;
	}
	public void setTimageUrl(String timageUrl) {
		this.timageUrl = timageUrl;
	}
	public String getDimageUrl() {
		return dimageUrl;
	}
	public void setdimageUrl(String dImageUrl) {
		this.dimageUrl = dImageUrl;
	}
	public int getTprice() {
		return tprice;
	}
	public void setTprice(int tprice) {
		this.tprice = tprice;
	}
	public int getDprice() {
		return dprice;
	}
	public void setDprice(int dprice) {
		this.dprice = dprice;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
